// ============================================================================
// Example 6: Auto-save Feature
// ============================================================================
export function setupAutoSave(
  getGameState: () => any,
  intervalMinutes: number = 5
) {
  const interval = setInterval(() => {
    try {
      const state = getGameState();
      const content = JSON.stringify(state, null, 2);
      const timestamp = new Date().toISOString().replace(/[:.]/g, '-').slice(0, -5);
      
      downloadTextFile(content, `autosave-${timestamp}.txt`);
      console.log('Game auto-saved successfully');
    } catch (error) {
      console.error('Auto-save failed:', error);
    }
  }, intervalMinutes * 60 * 1000);

  // Return cleanup function
  return () => clearInterval(interval);
}

// ============================================================================
// Example 7: Export Game as PGN-style Notation
// ============================================================================
export function exportAsPGN(gameMetadata: {
  event?: string;
  site?: string;
  date: Date;
  round?: string;
  black: string;
  white: string;
  result: string;
  moves: string[];
}) {
  const content = `[Event "${gameMetadata.event || 'Casual Game'}"]
[Site "${gameMetadata.site || 'Shogi Companion'}"]
[Date "${gameMetadata.date.toISOString().slice(0, 10)}"]
[Round "${gameMetadata.round || '1'}"]
[Black "${gameMetadata.black}"]
[White "${gameMetadata.white}"]
[Result "${gameMetadata.result}"]

${gameMetadata.moves.join(' ')}
`;

  downloadTextFile(content, 'shogi-game.pgn');
}

