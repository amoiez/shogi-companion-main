/**
 * Practical examples of integrating text file download into the Shogi game
 * These examples show real-world use cases specific to this application
 */

import { downloadTextFile } from '@/lib/utils';
import type { CellData } from '@/hooks/useGameState';

// ============================================================================
// Example 1: Export Game in SFEN Format
// ============================================================================
export function exportGameAsSFEN(boardState: CellData[][], moveHistory: string[]) {
  const sfenLines = [
    '# Shogi Game Export',
    `# Date: ${new Date().toLocaleString()}`,
    '# Format: SFEN',
    '',
    '# Initial Position',
    'startpos',
    '',
    '# Moves',
    moveHistory.join(' '),
    '',
    '# End of Game'
  ];

  const content = sfenLines.join('\n');
  const filename = `shogi-game-${Date.now()}.sfen`;
  
  downloadTextFile(content, filename);
}

// ============================================================================
// Example 2: Export Move History as Readable Text
// ============================================================================
export function exportMoveHistory(moves: Array<{
  moveNumber: number;
  player: 'black' | 'white';
  notation: string;
  timestamp: Date;
}>) {
  const header = `将棋 Move History
Generated: ${new Date().toLocaleString()}
Total Moves: ${moves.length}
${'='.repeat(50)}
`;

  const moveLines = moves.map(move => {
    const playerSymbol = move.player === 'black' ? '☗' : '☖';
    const time = move.timestamp.toLocaleTimeString();
    return `${move.moveNumber}. ${playerSymbol} ${move.notation} (${time})`;
  });

  const content = header + '\n' + moveLines.join('\n');
  downloadTextFile(content, 'move-history.txt');
}

